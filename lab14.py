# Дан русский текст. Текст может состоять из любых символов, вам
# необходимо транслитерировать (то есть заменить все русские буквы на английские)
# только русские буквы, а остальные оставить на месте. Строчные буквы заменяются
# на строчные, заглавные заменяются на заглавные. Если заглавная буква
# превращается при транслитерации в несколько букв, то заглавной должна остаться
# только первая из них (например, «Ц» → «Tc»).
# Правила трансляции:
# А – A
# Б - B
# В - V
# Г - G
# Д - D
# Е - E
# Ё - E
# Ж - ZH
# З - Z
# И - I
# Й - I
# К - K
# Л - L
# М - M
# Н - N
# О - O
# П - P
# Р - R
# С - S
# Т - T
# У - U
# Ф - F
# Х - KH
# Ц - TC
# Ч - CH
# Ш - SH
# Щ - SHCH
# Ы - Y
# Ъ -
# Ь -
# Э - E
# Ю - IU
# Я - IA


translitDict = {
    'А': 'A', 'Б': 'B', 'В': 'V', 'Г': 'G', 'Д': 'D', 'Е': 'E', 'Ё': 'E', 'Ж': 'ZH', 'З': 'Z',
    'И': 'I', 'Й': 'I', 'К': 'K', 'Л': 'L', 'М': 'M', 'Н': 'N', 'О': 'O', 'П': 'P', 'Р': 'R',
    'С': 'S', 'Т': 'T', 'У': 'U', 'Ф': 'F', 'Х': 'KH', 'Ц': 'TC', 'Ч': 'CH', 'Ш': 'SH', 'Щ': 'SHCH',
    'Ы': 'Y', 'Ъ': '', 'Ь': '', 'Э': 'E', 'Ю': 'IU', 'Я': 'IA',
    'а': 'a', 'б': 'b', 'в': 'v', 'г': 'g', 'д': 'd', 'е': 'e', 'ё': 'e', 'ж': 'zh', 'з': 'z',
    'и': 'i', 'й': 'i', 'к': 'k', 'л': 'l', 'м': 'm', 'н': 'n', 'о': 'o', 'п': 'p', 'р': 'r',
    'с': 's', 'т': 't', 'у': 'u', 'ф': 'f', 'х': 'kh', 'ц': 'tc', 'ч': 'ch', 'ш': 'sh', 'щ': 'shch',
    'ы': 'y', 'ъ': '', 'ь': '', 'э': 'e', 'ю': 'iu', 'я': 'ia'
}

text = input("Введите русский текст: ")

transliteratedText = ''
for char in text:
    if char in translitDict:
        transliteratedText += translitDict[char]
    else:
        transliteratedText += char

print("Транслитерированный текст:", transliteratedText)
